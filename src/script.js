// Helper functions
export const getUniversitiesByProvince = (province) => {
    return southAfricanUniversities.filter(uni => uni.province === province);
};

export const searchUniversities = (query) => {
    return southAfricanUniversities.filter(uni => 
        uni.name.toLowerCase().includes(query.toLowerCase()) ||
        uni.city.toLowerCase().includes(query.toLowerCase()) ||
        uni.description.toLowerCase().includes(query.toLowerCase())
    );
};

export const getUniversityById = (id) => {
    return southAfricanUniversities.find(uni => uni.id === id);
};

export const getUniversitiesWithProspectus = () => {
    return southAfricanUniversities.filter(uni => uni.prospectusUrl);
};

