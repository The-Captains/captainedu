// Helper functions
export const getBursariesByCategory = (category) => {
    return bursaries.filter(b => b.category === category);
};

export const getBursariesByLevel = (level) => {
    return bursaries.filter(b => b.level === level);
};

export const searchBursaries = (query) => {
    return bursaries.filter(b => 
        b.name.toLowerCase().includes(query.toLowerCase()) ||
        b.provider.toLowerCase().includes(query.toLowerCase()) ||
        b.description.toLowerCase().includes(query.toLowerCase())
    );
};

export const getUpcomingDeadlines = () => {
    return bursaries.filter(b => {
        const deadline = new Date(b.deadline);
        const now = new Date();
        const daysUntil = (deadline - now) / (1000 * 60 * 60 * 24);
        return daysUntil > 0 && daysUntil < 90;
    }).sort((a, b) => new Date(a.deadline) - new Date(b.deadline));
};

